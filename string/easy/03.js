/*

বিভিন্ন প্রোগ্রামিং ভাষায় "!=="" অপারেটর ব্যবহার করে সমান নয় তার যাচাই করার স্থানে ব্যবহার করা হয়। নোড জেএস, জাভাস্ক্রিপ্ট, পাইথন, এবং অন্যান্য প্রোগ্রামিং ভাষাগুলি এই ধরনের অপারেটর সাপোর্ট করে।

এটি নোড জেএস এক্সাম্পল দেওয়া যাকঃ

```javascript
// এই স্টেটমেন্ট সত্য হবে, কারণ 1 সমান নয় -1
console.log(1 !== -1); // আউটপুট: true

// এই স্টেটমেন্ট মিথ্যা হবে, কারণ 1 সমান নয় 1
console.log(1 !== 1); // আউটপুট: false
```

এখানে "!=="" অপারেটরটি যদি সত্য হয়, তাহলে এই মান দুটি সমান নয়। আরও সুস্পষ্টভাবে বলতে গেলে, "!=="" অপারেটরটি সমান নয় এবং মানে "সমান নয়" না।

উপরের উদাহরণে, "1 !== -1" সত্য কারণ 1 এবং -1 দুটি ভিন্ন মান এবং "1 !== 1" মিথ্যা কারণ কারণ দুইটি মান সমান।


*/
